    {
        //
        $comment=Comment::create([
            'body'=>$request->body,
            'user_id'=>Auth::id(),
            'post_id'=>$request->post_id
        ]);

        return response::json([ 
            'id'=>$comment->id,
            'body'=>$comment->body,
            'user_id'=>$comment->user_id,
            'created_at'=>$comment->created_at->diffForHumans(),
        ]);
    }

